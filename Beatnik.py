beatnik_poetry = """1. Καφές στο Εξάρχεια
Καφές δίχως ζάχαρη,
οι τοίχοι μουρμουρίζουν επαναστάσεις
και ο γέρος με το πούρο
ξέρει κάτι για τον κόσμο που ξεχάσαμε
μα δε θα το πει.

2. Μηχανή στον Υμηττό
Γκάζια, φλόγα και ομίχλη·
ο αέρας τρίζει σαν βινύλιο,
με κάθε στροφή
χάνομαι
και βρίσκω τη φθορά
σε μια ανάσα που μυρίζει βενζίνη.

3. Αστικό Χάος
Ένα φανάρι τρεμοπαίζει,
τα ταξί κοιμούνται·
κάποιος παίζει σαξόφωνο με βλέμμα χαμένο
κι εγώ,
καπνίζω το τίποτα
περιμένοντας την αυγή που δε θέλει να έρθει.

4. Jazz στην Πανεπιστημίου
Σκιά από τρομπέτα
χαράζει τη σκόνη του δρόμου,
η πόλη αναστενάζει μπλουζ
κι ο τύπος με το καπέλο
ξέρει πως ο Θεός
είναι και ντράμερ.
το μόνο που λείπει είναι να βρούν,
το σωστό ρυθμό.

5. Σκέψεις σε ταράτσα
Το σύμπαν απλώνεται,
μα εγώ καρφώνω το βλέμμα μου σε ένα παράθυρο απέναντι·
μια γάτα με κοιτάει
και μου λέει περισσότερα
απ’ ό,τι όλοι οι ψυχολόγοι μου.

6. Ηλεκτρικό Σούρουπο
Το φως του neon
χαϊδεύει την απελπισία,
οι σκιές βγάζουν τσιγάρο
και φιλιούνται στα κρυφά,
ενώ ο ήλιος θαμπώνει στο Σαρωνικό.

7. Νυχτερινό σε ρυθμό τύμπανου
Σώματα χορεύουν σαν μεθυσμένα ποιήματα,
η μουσική ανεβαίνει από τα πεζοδρόμια,
κι ο ποιητής γράφει με τα μάτια κλειστά,
γιατί μόνο έτσι
μπορεί να δει την αλήθεια.

8. Φορτηγό στην Εθνική
Ο ήλιος μπήγεται σαν πρόκα στο μέτωπο,
ο οδηγός μασάει τσίχλα και μνήμες,
η Εθνική δρόμος-πληγή,
κι εγώ γράφω με λάδι και καπνό
το δικό μου ιερό ημερολόγιο.

9. Η Ποίηση Είναι Ραδιενέργεια
Σκέψεις θρυμματισμένες,
λέξεις γεμάτες ισότοπα,
η ποίηση είναι αντίδραση
δίχως σταθερό πυρήνα –
κι όμως:
σώζει ψυχές από αργό θάνατο.

10. Κουμπί στο πάτωμα
Βρήκα ένα κουμπί στο πάτωμα
κι ένιωσα ότι ο κόσμος
ξεκουμπώθηκε.
Η πόρτα έτριξε σαν ξεχασμένος στίχος,
οι σκέψεις μου έτρεξαν
σαν κατσαρίδες στο φως.
Ένα τσιγάρο σβησμένο πριν την ώρα του,
μια γυναίκα με βλέμμα σαν
παλιό ασανσέρ που δεν φτάνει ποτέ
στο ρετιρέ της ψυχής.
Ο ήχος της μηχανής του ψυγείου
μου θύμισε τον ρυθμό της καρδιάς μου:
μπλουζ χωρίς μελωδία,
παρά μόνο μια ατέρμονη προσμονή
για κάτι που δε θα γίνει ποτέ
κι όμως…
γράφω."""
print(beatnik_poetry)
